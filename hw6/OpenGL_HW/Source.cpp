void keyboard(unsigned char key, int x, int y)
{
	switch (key) {
	case 27: //esc
		exit(0);
		break;

	case 49: //1
		derefMethod = DRAWARRAYS;
		glutPostRedisplay(); //重要，這樣才能重新display
		break;

	case 50: //2
		derefMethod = DRAWELEMENTS;
		glutPostRedisplay();

		break;

	case 51: //3
		derefMethod = MULTIDRAWARRAYS;
		glutPostRedisplay();
		break;

	case 52: //4
		derefMethod = MULTIDRAWELEMENTS;
		glutPostRedisplay();
		break;

	//相機移動
	case 'w':
		//++Snipey;
		pos_z--;
		glutPostRedisplay();
		break;
	case 's':
		//--Snipey;
		pos_z++;
		glutPostRedisplay();
		break;
	case 'a':
		//--Snipex;
		pos_x--;
		glutPostRedisplay();
		break;
	case 'd':
		//++Snipex;
		pos_x++;
		glutPostRedisplay();
		break;

	//手臂角度
	case 'y':
		leftArm.elbowy++;
		glutPostRedisplay();
		break;
	case 'h':
		leftArm.elbowy--;
		glutPostRedisplay();
		break;

	case 'u':
		leftArm.elbowz++;
		glutPostRedisplay();
		break;
	case 'j':
		leftArm.elbowz--;
		glutPostRedisplay();
		break;

	case 'i':
		leftArm.shouldery++;
		glutPostRedisplay();
		break;
	case 'k':
		leftArm.shouldery--;
		glutPostRedisplay();
		break;

	case 'o':
		leftArm.shoulderz--;
		glutPostRedisplay();
		break;
	case 'l':
		leftArm.shoulderz++;
		glutPostRedisplay();
		break;
	
	case 32: //space
		jump = true;
		break;

	case 'x':
		rightArm.swing_hand = true;
		break;

	case 'z':
		leftArm.swing_hand = true;
		break;

	case 'r':
		for(int i=0;i<23;++i)
			bBox[i].show = true;
		break;
	//hw6
	case 'b':
		snipe = !snipe;
		break;
	case 'f':
		fog = !fog;
		if (fog) {
			glClearColor(0.5, 0.5, 0.5, 0.5);  /* fog color */
			GLfloat fogColor[4] = { 0.5, 0.5, 0.5, 1.0 };
			glFogfv(GL_FOG_COLOR, fogColor);
			glEnable(GL_FOG);
		}
		else {
			glClearColor(0, 0, 0, 1.0);  /* fog color */
			glDisable(GL_FOG);
		}
		
		break;

	case 'p':
		offset = !offset;
		if (offset) {
			glPolygonOffset(5, 2);
		}
		else {
			glPolygonOffset(-1, 2);
		}

		break;

	case 'c':
		antiAliasing = !antiAliasing;
		if (antiAliasing) {
			//anti-aliasing 
			glEnable(GL_LINE_SMOOTH);
			glEnable(GL_POLYGON_SMOOTH);
			glHint(GL_LINE_SMOOTH_HINT, GL_DONT_CARE);
			glHint(GL_POLYGON_SMOOTH_HINT, GL_DONT_CARE);
		}
		else {
			glDisable(GL_LINE_SMOOTH);
			glDisable(GL_POLYGON_SMOOTH);
		}

		break;
	}

	
	//printf("%f,%f,%f\n", arm_x, arm_y, arm_z);
	//printf("%f,%f,%f,%f\n", leftArm.shoulderz, leftArm.shouldery, leftArm.elbowz, leftArm.elbowy);
	//printf("camera_pos_z: %f\n", pos_z);

}

