void init() {
	glClearColor(0.0, 0.0, 0.0, 0.0);
	//glShadeModel(GL_FLAT); //默認為GL_SMOOTH(自行計算過度色)，GL_FLAT不會有過度色(混色)
	glEnableClientState(GL_VERTEX_ARRAY); //要記得enable才能使用array
	glEnableClientState(GL_COLOR_ARRAY); //要記得enable才能使用array
	glEnable(GL_CULL_FACE); //all the faces that are not front-faces are discarded -> 確認都為正面
	glEnable(GL_DEPTH_TEST);
	setupVertexAndColor(); //設定全域陣列，供所有函數使用
	glutIdleFunc(spinDisplay);

	//light
	GLfloat position[] = { 1, 1, 0, 0.0 };
	glLightfv(GL_LIGHT0, GL_POSITION, position);
	glEnable(GL_LIGHTING);
	glEnable(GL_LIGHT0);

	glEnable(GL_COLOR_MATERIAL);

	//fog
	glEnable(GL_FOG);
	{
		GLfloat fogColor[4] = { 0.5, 0.5, 0.5, 1.0 };

		//fogMode = GL_EXP;
		glFogi(GL_FOG_MODE, GL_EXP);
		glFogfv(GL_FOG_COLOR, fogColor);
		glFogf(GL_FOG_DENSITY, 0.05);
		glHint(GL_FOG_HINT, GL_DONT_CARE);
		glFogf(GL_FOG_START, -10);
		glFogf(GL_FOG_END, 10);
	}
	glDisable(GL_FOG);

	//polyoffset
	glEnable(GL_POLYGON_OFFSET_FILL);
	glPolygonOffset(-1, 2);

	//blend
	glEnable(GL_BLEND);
	glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);

	//monster
	glEnable(GL_RESCALE_NORMAL);

	theMonster = glGenLists(1);
	glNewList(theMonster, GL_COMPILE);
	drawMonster();
	glEndList();
}

void reshape(int w, int h)
{
	glViewport(0, 0, (GLsizei)w, (GLsizei)h);
	glMatrixMode(GL_PROJECTION);
	glLoadIdentity();
	glFrustum(-2.0, 2.0, -2.0, 2.0, 1, 60.0);//3D Perspective projection
	glMatrixMode(GL_MODELVIEW);
	glLoadIdentity();
}

