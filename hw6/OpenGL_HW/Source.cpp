class Monster {
public:
	GLfloat x, y, z, blood;
	bool moveR,moveF,focus,harm;
	Monster(GLfloat x, GLfloat y, GLfloat z, GLfloat blood,bool moveR) {
		this->x = x;
		this->y = y;
		this->z = z;
		this->blood = blood;
		this->moveR = moveR;
		this->moveF = true;
		this->focus = false;
	}

	void draw(GLfloat systemPosX,GLfloat systemPosZ) {
		if (blood) {
			glPushMatrix();
			{
				glTranslated(this->x, this->y, this->z);
				glCallList(theMonster);
				glDisable(GL_LIGHTING);
				{
					glColor3f(1, 0, 0);
					glTranslated(-blood/2, -6, 0);
					glRectd(0, 0, blood, 1);
				}
				glEnable(GL_LIGHTING);
				
			}
			glPopMatrix();

			if (systemMove && fabs(systemPosX - this->x) < 9 && fabs(systemPosZ - this->z) < 0.1) {
				systemMove = false;
				systemPos = 0;
				
				PlaySound(TEXT("被擊中的慘叫.wav"), NULL, SND_ASYNC);
				--this->blood;
				focus = true;
				printf("blood\n");
			}

			if (focus) {
				if (systemPosZ - this->z > 0) this->z += 0.005;
				else this->z += 0.005;
				if (systemPosX - this->x > -7) this->x += 0.005;
				else this->x -= 0.005;
			}
			else {
				if (moveR) this->x += 0.01;
				else this->x -= 0.01;

				if (this->x > 35) moveR = false;
				if (this->x < -35) moveR = true;
			}
			
			if (!moveF) this->z -= 0.2;
			if (systemPosZ - this->z < 5) this->focus = true;
			if (systemPosZ - this->z > 3) moveF = true;

			if (pos_z - arm_z - 5 - this->z < -1) {
				moveF = false; isAttack = true;
				PlaySound(TEXT("男慘叫聲.wav"), NULL, SND_ASYNC);
			}
		}
		//printf("monster x=%f, z=%f\n", this->x, this->z);
		//printf("abs x=%f, z=%f\n", fabs(systemPosX - this->x), fabs(systemPosZ - this->z));
		
	}
};
