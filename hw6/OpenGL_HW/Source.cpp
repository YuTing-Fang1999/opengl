void spinDisplay(void)
{

	year = year - (float)360 / 365 /10; 
	if (year < 0) year = 360;
	day = day + (float)360 / 100 /10;
	if (day > 360) day = 0;
	glutPostRedisplay();

	if (derefSpin == SPIN_LOGO) {
		spin = spin - 0.5;
		if (spin < 0) spin = spin + 360.0;
	}

	//change color
	if ((int)year % 30 == 0) {
		float r = rand() % 256 / (float)256;
		float g = rand() % 256 / (float)256;
		float b = rand() % 256 / (float)256;
		for (int i=0; i < 48; i+=3) {
			colors[0][i] = r;
			colors[0][i+1] = g;
			colors[0][i+2] = b;
		}
	}

	if (jump) {
		if (up) {
			pos_y += 0.1;
		}
		else {
			pos_y -= 0.1;
		}

		if (pos_y > 5) up = false;
		if (pos_y < -7) {
			pos_y = -7;
			jump = false;
			up = true;
		}
	}

	if (rightArm.swing_hand) {
		if (rightArm.swing_hand_2) {
			rightArm.shoulderz -= 0.3;
		}
		else {
			rightArm.shoulderz += 0.3;
		}

		if (rightArm.shoulderz <-110) rightArm.swing_hand_2 = false;
		if (rightArm.shoulderz > -55) {
			rightArm.shoulderz = -55;
			rightArm.swing_hand = false;
			rightArm.swing_hand_2 = true;
		}
	}

	if (leftArm.swing_hand) {
		if (leftArm.swing_hand_2) systemMove = true;
		if (leftArm.swing_hand_2) {
			leftArm.shouldery += 8;
		}
		else {
			leftArm.shouldery -= 1;
		}

		if (leftArm.shouldery > 280) leftArm.swing_hand_2 = false;
		if (leftArm.shouldery < 173) {
			leftArm.shouldery = 173;
			leftArm.swing_hand = false;
			leftArm.swing_hand_2 = true;
		}
	}

	if (systemMove) {
		systemPos -= 0.1;
		if (systemPos < -50) {
			systemMove = false;
			systemPos = 0;
			printf("system x=%f, y=%f, z=%f\n", pos_x + arm_x - 15, pos_y + arm_y + 2.5, pos_z - arm_z - 5 + systemPos);

		}
	}
	
	glutPostRedisplay();
}

